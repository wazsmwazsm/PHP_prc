<?php

$a = [5 => 6, 2 => 1, 4 => 8, 1 => 2];

sort($a); // 重排索引
var_dump($a); // 0 => 1, 1 => 2, 2 => 6, 3 => 8

$a = [5 => 6, 2 => 1, 4 => 8, 1 => 2];

asort($a); // 不重排索引
var_dump($a); // 2 => 1, 1 => 2, 5 => 6, 4 => 8

$a = ['a' => 6, 'b' => 1, 'c' => 8, 'd' => 2];

sort($a); // 重排索引
var_dump($a); // 0 => 1, 1 => 2, 2 => 6, 3 => 8

$a = ['a' => 6, 'b' => 1, 'c' => 8, 'd' => 2];

asort($a); // 重排索引
var_dump($a); // 'b' => 1, 'd' => 2, 'a' => 6, 'c' => 8
